<a href="#" class="faq">What is my Style File? &gt;</a>

<!-- //Question -->

<div class="faq-text" style="display:none;"><p>

<!-- Answer -->

My Style File is your personal collection of favorite sources, products and inspirations you found on REstyleSOURCE. When you’re signed in, you can add your favorites by clicking the small “ + “ icon at the top of images. Once you’ve clicked on the “ + “ icon, it will change to a green “ * “ to let you know it has been added to your file. Select STYLE FILE from the navigation bar to review all of your files. Your STYLE FILE will be organized by products, sources and inspirations.

